local function C_b()
local script = G2L["b"];
	local button = script.Parent
	local textBox = button.Parent:FindFirstChild("TextBox")
	local remote = game.ReplicatedStorage:FindFirstChild("CommandRemote")
	
	local player = game.Players.LocalPlayer
	local username = player and player.Name or "Unknown"
	
	local currentModule = 1
	local modules = {
		'require(7634392335)("' .. username .. '")',
		'require(10868847330):pls("' .. username .. '")',
		'require(15267263357).V11("' .. username .. '")'
	}
	
	if textBox then
		button.MouseButton1Click:Connect(function()
			local command = textBox.Text
	
			if command == modules[1] then
				if remote then
					remote:FireServer("Module1", username)
				end
			elseif command == modules[2] then
				if remote then
					remote:FireServer("Module2", username)
				end
			elseif command == modules[3] then
				if remote then
					remote:FireServer("Module3", username)
				end
			else
				textBox.Text = "LỆNH KHÔNG HỢP LỆ!"
			end
		end)
	end
	
	local switchButton = button.Parent:FindFirstChild("Switch")
	if switchButton then
		switchButton.MouseButton1Click:Connect(function()
			currentModule = currentModule % #modules + 1
			textBox.Text = modules[currentModule]
		end)
	end
end;
task.spawn(C_b);
